package ch01.sec09; //생략불가, src 아래 하위 폴더 지정, 맨 윗줄

public class Hello { // 자바 파일명과 동일
    public static void main(String[] args) {
        // main 함수
        // 함수가 여러개일 때 main에서 무조건 시작
        System.out.println("Hello, Java");
        // System: 컴퓨터, .: 접근, out:출력장치(모니터 등), println: 띄어쓰기해서 출력
    }
}
